    private JLabel label;
    private Timer timer;
    private int counter = 10; // the duration
    private int delay = 1000; // every 1 second
    private static final long serialVersionUID = 1L;
    
    public RegisterComplete() {
        super("Simple Timer");
        initComponents();
        addWindowListener(new WindowAdapter() {
        
            @Override
            public void windowClosing(WindowEvent e) {
                JOptionPane.showMessageDialog(null, "Du kan ikke lukke programmet p√• dette tidspunktet");
            }
        });
            
        label = new JLabel("Takk for at du registrerte deg" + counter + "sec", JLabel.CENTER);
        JPanel contentPane = (JPanel) getContentPane();
        contentPane.add(label, BorderLayout.CENTER);
        contentPane.setBorder(BorderFactory.createEmptyBorder(5, 5, 5, 5));
        pack();
        
        ActionListener action = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent event) {
                if (counter == 0) {
                    timer.stop();
                    slit.ejb.login.Login loginFrame = new slit.ejb.login.Login();
                    loginFrame.setVisible(true);
                    dispose();
                    label.setText("Boom");
                }
                else {
                    label.setText("Takk for at du registrerte deg." +" " + 
                            "Du vil bli sendt tilbake til innlogging om" + " "
                            +counter + " sekunder");
                    counter--;
                }
            }
        };
        
        timer = new Timer(delay, action);
        timer.setInitialDelay(0);
        timer.start();
        
        setVisible(true);
        }
    

